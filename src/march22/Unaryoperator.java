package march22;

public class Unaryoperator{
	 public static void main(String args[]) 
	 {
		 int q=1;
		 System.out.println(q++);
		 System.out.println(q++);
		 System.out.println(q=-q);
		 
		 System.out.println(--q);
		 
		 
		 System.out.println(q--);
		 System.out.println(++q);
		 
		 
		 System.out.println(q--);
		 System.out.println(q--);
		 System.out.println(++q);
		 
		 
	 }

	
}
