package march22;

public class Relational {
	public static void main(String[] args) {
		int a=200,b=100;
		
		boolean result=a==b;
		System.out.println(result);
		
		boolean r=a!=b;
		System.out.println(r);
		
		boolean s=a>b;
		System.out.println(s);
		
		boolean t=a<b;
		System.out.println(t);
		
		boolean u=a>=b;
		System.out.println(u);
		
		boolean v=a<=b;
		System.out.println(v);
		
		
	}

}
