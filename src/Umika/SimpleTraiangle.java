package Umika;

public class SimpleTraiangle {

    public static void main(String[] args)
	    {int row = 5;
	     
	        //Printing the pattern
	   {
	        	
	        	for (int a=0; a<row; a++)
	        	{
	        	for (int b=row-a; b>0; b--)
	        	{
	        	System.out.print(" ");
	        	}
	        
	        	for (int b=0; b<=a; b++ )
	        	{
	        
	        	System.out.print("* ");
	        	}
	        	System.out.println();
	        	}
	   }
	    }
	     

}
  